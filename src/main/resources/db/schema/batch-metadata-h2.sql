CREATE TABLE IF NOT EXISTS BATCH_JOB_INSTANCE (
  JOB_INSTANCE_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  VERSION BIGINT,
  JOB_NAME VARCHAR(100) NOT NULL,
  JOB_KEY VARCHAR(32) NOT NULL,
  CONSTRAINT JOB_INST_UN UNIQUE (JOB_NAME, JOB_KEY)
);

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION (
  JOB_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  VERSION BIGINT,
  JOB_INSTANCE_ID BIGINT NOT NULL,
  CREATE_TIME TIMESTAMP(9) NOT NULL,
  START_TIME TIMESTAMP(9) DEFAULT NULL,
  END_TIME TIMESTAMP(9) DEFAULT NULL,
  STATUS VARCHAR(10),
  EXIT_CODE VARCHAR(2500),
  EXIT_MESSAGE VARCHAR(2500),
  LAST_UPDATED TIMESTAMP(9),
  CONSTRAINT JOB_INST_EXEC_FK FOREIGN KEY (JOB_INSTANCE_ID)
    REFERENCES BATCH_JOB_INSTANCE(JOB_INSTANCE_ID)
);

CREATE INDEX IF NOT EXISTS idx_batch_job_execution_retention
  ON BATCH_JOB_EXECUTION(STATUS, END_TIME, JOB_INSTANCE_ID);

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION_PARAMS (
  JOB_EXECUTION_ID BIGINT NOT NULL,
  PARAMETER_NAME VARCHAR(100) NOT NULL,
  PARAMETER_TYPE VARCHAR(100) NOT NULL,
  PARAMETER_VALUE VARCHAR(2500),
  IDENTIFYING CHAR(1) NOT NULL,
  CONSTRAINT JOB_EXEC_PARAMS_FK FOREIGN KEY (JOB_EXECUTION_ID)
    REFERENCES BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

CREATE TABLE IF NOT EXISTS BATCH_STEP_EXECUTION (
  STEP_EXECUTION_ID BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  VERSION BIGINT NOT NULL,
  STEP_NAME VARCHAR(100) NOT NULL,
  JOB_EXECUTION_ID BIGINT NOT NULL,
  CREATE_TIME TIMESTAMP(9) NOT NULL,
  START_TIME TIMESTAMP(9) DEFAULT NULL,
  END_TIME TIMESTAMP(9) DEFAULT NULL,
  STATUS VARCHAR(10),
  COMMIT_COUNT BIGINT,
  READ_COUNT BIGINT,
  FILTER_COUNT BIGINT,
  WRITE_COUNT BIGINT,
  READ_SKIP_COUNT BIGINT,
  WRITE_SKIP_COUNT BIGINT,
  PROCESS_SKIP_COUNT BIGINT,
  ROLLBACK_COUNT BIGINT,
  EXIT_CODE VARCHAR(2500),
  EXIT_MESSAGE VARCHAR(2500),
  LAST_UPDATED TIMESTAMP(9),
  CONSTRAINT JOB_EXEC_STEP_FK FOREIGN KEY (JOB_EXECUTION_ID)
    REFERENCES BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

CREATE TABLE IF NOT EXISTS BATCH_STEP_EXECUTION_CONTEXT (
  STEP_EXECUTION_ID BIGINT NOT NULL PRIMARY KEY,
  SHORT_CONTEXT VARCHAR(2500) NOT NULL,
  SERIALIZED_CONTEXT LONGVARCHAR,
  CONSTRAINT STEP_EXEC_CTX_FK FOREIGN KEY (STEP_EXECUTION_ID)
    REFERENCES BATCH_STEP_EXECUTION(STEP_EXECUTION_ID)
);

CREATE TABLE IF NOT EXISTS BATCH_JOB_EXECUTION_CONTEXT (
  JOB_EXECUTION_ID BIGINT NOT NULL PRIMARY KEY,
  SHORT_CONTEXT VARCHAR(2500) NOT NULL,
  SERIALIZED_CONTEXT LONGVARCHAR,
  CONSTRAINT JOB_EXEC_CTX_FK FOREIGN KEY (JOB_EXECUTION_ID)
    REFERENCES BATCH_JOB_EXECUTION(JOB_EXECUTION_ID)
);

CREATE TABLE IF NOT EXISTS STOCK_BATCH_JOB_METADATA_ARCHIVE (
  JOB_EXECUTION_ID BIGINT PRIMARY KEY,
  JOB_INSTANCE_ID BIGINT NOT NULL,
  JOB_NAME VARCHAR(100) NOT NULL,
  JOB_KEY VARCHAR(32) NOT NULL,
  STATUS VARCHAR(10) NOT NULL,
  CREATE_TIME TIMESTAMP(9) NOT NULL,
  START_TIME TIMESTAMP(9) DEFAULT NULL,
  END_TIME TIMESTAMP(9) NOT NULL,
  EXIT_CODE VARCHAR(2500),
  EXIT_MESSAGE VARCHAR(2500),
  IDENTIFYING_PARAMETERS VARCHAR(4000) NOT NULL,
  STEP_COUNT BIGINT NOT NULL,
  READ_COUNT BIGINT NOT NULL,
  WRITE_COUNT BIGINT NOT NULL,
  FILTER_COUNT BIGINT NOT NULL,
  COMMIT_COUNT BIGINT NOT NULL,
  ROLLBACK_COUNT BIGINT NOT NULL,
  SKIP_COUNT BIGINT NOT NULL,
  ARCHIVED_AT TIMESTAMP(9) NOT NULL,
  PURGED_AT TIMESTAMP(9) DEFAULT NULL
);

CREATE INDEX IF NOT EXISTS idx_stock_batch_metadata_archive_instance
  ON STOCK_BATCH_JOB_METADATA_ARCHIVE(JOB_INSTANCE_ID);
CREATE INDEX IF NOT EXISTS idx_stock_batch_metadata_archive_time
  ON STOCK_BATCH_JOB_METADATA_ARCHIVE(ARCHIVED_AT, JOB_EXECUTION_ID);

CREATE SEQUENCE IF NOT EXISTS BATCH_STEP_EXECUTION_SEQ;
CREATE SEQUENCE IF NOT EXISTS BATCH_JOB_EXECUTION_SEQ;
CREATE SEQUENCE IF NOT EXISTS BATCH_JOB_INSTANCE_SEQ;
